function [value,gradient,hessian] = myPhi(x)
%this function is the phi function in the 3th section of q1 
%  R^3->R; analytic evaluations of the function, it's gradient and hessian
x = x(:);

value = sin(x(1)*x(2)*x(3));
gradient = [...
    x(2)*x(3)*cos(x(1)*x(2)*x(3));...
    x(1)*x(3)*cos(x(1)*x(2)*x(3));...
    x(1)*x(2)*cos(x(1)*x(2)*x(3))...
    ];

hessian = [...
    -((x(2)*x(3))^2)*sin(x(1)*x(2)*x(3)),x(3)*cos(x(1)*x(2)*x(3)) - x(1)*x(3)*x(2)*x(3)*sin(x(1)*x(2)*x(3)), x(2)*cos(x(1)*x(2)*x(3)) - (x(1)*x(2))*x(2)*x(3)*sin(x(1)*x(2)*x(3));...
    x(3)*cos(x(1)*x(2)*x(3)) - x(1)*x(3)*x(2)*x(3)*sin(x(1)*x(2)*x(3)),-((x(1)*x(3))^2)*sin(x(1)*x(2)*x(3)) , x(1)*cos(x(1)*x(2)*x(3)) - (x(1)*x(2))*x(1)*x(3)*sin(x(1)*x(2)*x(3));...
    x(2)*cos(x(1)*x(2)*x(3)) - (x(1)*x(2))*x(2)*x(3)*sin(x(1)*x(2)*x(3)),x(1)*cos(x(1)*x(2)*x(3)) - (x(1)*x(2))*x(1)*x(3)*sin(x(1)*x(2)*x(3)),-((x(1)*x(2))^2)*sin(x(1)*x(2)*x(3))...
    ];

end

